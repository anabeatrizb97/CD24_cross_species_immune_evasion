def ensure_siglec10_tam_score(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()

    if "SIGLEC10_TAM_score" in df.columns:
        return df

    pan_mac_genes = ["CD68", "CSF1R", "ITGAM", "FCGR3A", "ADGRE1"]
    valid = [g for g in pan_mac_genes if f"{g}_expr" in df.columns]
    if not valid:
        raise ValueError("Cannot compute macrophage score: none of CD68/CSF1R/ITGAM/FCGR3A/ADGRE1 found.")

    df["Macrophage_score"] = df[[f"{g}_expr" for g in valid]].mean(axis=1)
    if "SIGLEC10_expr" not in df.columns:
        raise ValueError("SIGLEC10_expr column missing; cannot compute SIGLEC10_TAM_score.")
    df["SIGLEC10_TAM_score"] = df["SIGLEC10_expr"] * df["Macrophage_score"]
    return df


def analyze_cd24_siglec10_stage_5yr(stage_data: pd.DataFrame, stage_name: str, endpoint: str = "OS"):
    if endpoint == "OS":
        time_col, event_col = "time", "event"
    elif endpoint == "PFI":
        time_col, event_col = "pfi_time", "pfi_event"
    else:
        raise ValueError(f"Unknown endpoint: {endpoint}")

    needed = [time_col, event_col, "CD24_SIGLEC10_group"]
    stage_data = stage_data.dropna(subset=needed).copy()
    if len(stage_data) < 20:
        return None, None, False, None

    stage_data[f"{time_col}_5yr"] = stage_data[time_col].clip(upper=60)
    stage_data[f"{event_col}_5yr"] = stage_data.apply(
        lambda row: 0 if row[time_col] > 60 else row[event_col], axis=1
    )

    groups_data = {g: stage_data[stage_data["CD24_SIGLEC10_group"] == g].copy()
                   for g in ["Low/Low", "Low/High", "High/Low", "High/High"]}

    total_events = int(stage_data[f"{event_col}_5yr"].sum())
    group_stats = {}
    for g in ["Low/Low", "Low/High", "High/Low", "High/High"]:
        n = len(groups_data[g])
        events = int(groups_data[g][f"{event_col}_5yr"].sum())
        group_stats[g] = {"n": n, "events": events, "censored": n - events, "event_rate": (events / n * 100) if n > 0 else 0}

    if total_events < 5:
        return groups_data, None, False, group_stats

    try:
        result = multivariate_logrank_test(
            stage_data[f"{time_col}_5yr"],
            stage_data["CD24_SIGLEC10_group"],
            stage_data[f"{event_col}_5yr"],
        )
    except Exception:
        return groups_data, None, False, group_stats

    result.time_col = f"{time_col}_5yr"
    result.event_col = f"{event_col}_5yr"
    return groups_data, result, True, group_stats



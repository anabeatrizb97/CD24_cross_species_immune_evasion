def build_master_survival_summary_table(
    results_cd24_os: dict,
    results_cd24_pfi: dict,
    results_combo_os: dict,
    results_combo_pfi: dict,
    has_pfi: bool,
) -> pd.DataFrame:
    """
    Compile a single 'master' survival summary table for all stage-stratified analyses.

    Notes on HR handling:
      - HR/CI are set to NA if HR is non-finite or > 50 (often due to complete separation).
      - P-values are kept whenever available.
    """
    rows = []

    def _to_float(x):
        if x is None:
            return np.nan
        try:
            return float(x)
        except Exception:
            s = str(x)
            m = re.search(r"[-+]?(?:\d*\.\d+|\d+)", s)
            return float(m.group(0)) if m else np.nan

    def add_cd24(results: dict, endpoint: str):
        for stage, payload in results.items():
            stats = payload.get("stats") or {}
            res = payload.get("result")
            row = {
                "analysis": "CD24",
                "stage": stage,
                "endpoint": endpoint,
                "logrank_p": _to_float(getattr(res, "p_value", np.nan)) if res is not None else np.nan,
                "Low_n": stats.get("Low", {}).get("n", np.nan),
                "High_n": stats.get("High", {}).get("n", np.nan),
                "Low_events": stats.get("Low", {}).get("events", np.nan),
                "High_events": stats.get("High", {}).get("events", np.nan),
                "HR_High_vs_Low": np.nan,
                "HR_CI_low": np.nan,
                "HR_CI_high": np.nan,
                "HR_p": np.nan,
            }

            if res is not None and hasattr(res, "hr") and res.hr is not None:
                hr_raw, ci_l_raw, ci_h_raw, p_raw = res.hr
                hr = _to_float(hr_raw)
                ci_l = _to_float(ci_l_raw)
                ci_h = _to_float(ci_h_raw)
                p_hr = _to_float(p_raw)

                # mask huge/unstable HR
                if (not np.isfinite(hr)) or (hr > 50):
                    hr, ci_l, ci_h, p_hr = np.nan, np.nan, np.nan, np.nan

                row.update({
                    "HR_High_vs_Low": hr,
                    "HR_CI_low": ci_l,
                    "HR_CI_high": ci_h,
                    "HR_p": p_hr,
                })

            rows.append(row)

    def add_combo(results: dict, endpoint: str):
        groups = ["Low/Low", "Low/High", "High/Low", "High/High"]
        for stage, payload in results.items():
            stats = payload.get("stats") or {}
            res = payload.get("result")
            row = {
                "analysis": "CD24_SIGLEC10_TAM",
                "stage": stage,
                "endpoint": endpoint,
                "multivariate_logrank_p": _to_float(getattr(res, "p_value", np.nan)) if res is not None else np.nan,
            }
            for g in groups:
                row[f"{g}_n"] = stats.get(g, {}).get("n", np.nan)
                row[f"{g}_events"] = stats.get(g, {}).get("events", np.nan)
            rows.append(row)

    add_cd24(results_cd24_os, "OS")
    if has_pfi and results_cd24_pfi:
        add_cd24(results_cd24_pfi, "PFI")

    add_combo(results_combo_os, "OS")
    if has_pfi and results_combo_pfi:
        add_combo(results_combo_pfi, "PFI")

    df_out = pd.DataFrame(rows)
    preferred = [
        "analysis", "stage", "endpoint",
        "logrank_p", "multivariate_logrank_p",
        "Low_n", "High_n", "Low_events", "High_events",
        "HR_High_vs_Low", "HR_CI_low", "HR_CI_high", "HR_p",
        "Low/Low_n", "Low/Low_events", "Low/High_n", "Low/High_events",
        "High/Low_n", "High/Low_events", "High/High_n", "High/High_events",
    ]
    cols = [c for c in preferred if c in df_out.columns] + [c for c in df_out.columns if c not in preferred]
    return df_out[cols]

